"""----------------------------------------------------------Syntax--------------------------------------
            frozenset([iterable])
            here frozenset is construction . if you want to create a frozen set using a constructor
            
            There is no literal syntax for frozenset in Python like there is for set using {...}
--------------------------------------------------------------------------------------------------------"""


fs = frozenset([1, 2, 3, 4])
print(fs)  # Output: frozenset({1, 2, 3, 4})